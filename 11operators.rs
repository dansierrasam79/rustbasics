// Common operators used in Rust
fn main(){
    let a: i32 = 5;
    let b: i32 = 12;
    let x: bool = true;
    let y: bool = false;
    println!("{}", a + b);
    println!("{}", a - b);
    println!("{}", a * b);
    println!("{}", a / b);
    println!("{}", a % b);
    println!("{}", a < b);
    println!("{}", a > b);
    println!("{}", !x);
    println!("{}", x & y);
    println!("{}", x | y);
    println!("{}", x == y);
    println!("{}", x != y);
}