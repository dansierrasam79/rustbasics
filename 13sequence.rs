
fn main() {

    let value: i32 = 5;
    let value2: i32 = 3;
    let add_result: i32 = value + value2;
    let sub_result: i32 = value2 - value;
    let prod_result: i32 = value * value2;
    let mod_result: i32 = value2%value;
    println!("Sum: {}", add_result);
    println!("Difference: {}", sub_result);
    println!("Product: {}", prod_result);
    println!("Remainder: {}", mod_result);

}